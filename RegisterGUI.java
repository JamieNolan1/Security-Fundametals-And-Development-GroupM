    //take password + salt > hashes using hashSha256 > returns as base64
     private String hashSha256(String password, byte[] salt) throws Exception
     {
        MessageDigest messageDigest = MessageDigest.getInstance("SHA-256");
        messageDigest.update(salt);
        byte[] hash = messageDigest.digest(password.getBytes());
        return Base64.getEncoder().encodeToString(hash);        //convert hash into base64 to save
     }
    
    
