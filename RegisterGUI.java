    private void CreateAccountButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_CreateAccountButtonActionPerformed
    {//GEN-HEADEREND:event_CreateAccountButtonActionPerformed
    // TODO add your handling code here: // CREATE ACCOUNT BUTTON SHOULD HASH + ENCRYPT AND SAVE TO TXT FILE
        
    //takes value sfrom text input
    String username = UsernameInput.getText().trim();
    String password = new String(PasswordInput.getPassword());
    String confirm = new String(ConfirmPasswordInput.getPassword());
    String email = EmailInput.getText().trim();
    String phone = PhoneNumberInput.getText().trim();
    String secQ = SecurityQInput.getText().trim();
    
    //check inputs are empty and if true then return error message "pls fill in all fielsd"
    if (username.isEmpty() || password.isEmpty() || confirm.isEmpty() || email.isEmpty() || phone.isEmpty() || secQ.isEmpty())
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields");
            return;
        }
   
    //check if passwords on input match
    if (!password.equals(confirm))
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Passwords do not match");
            return;
        }
    
    //check if email is valid format, (x@mail.com) 
    if (!email.matches("^[^@\\s]+@[^@\\s]+\\.[^@\\s]+$")) //regex
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid email address");
            return;
        }
    
    //phone number must be numbers (0-9) and between 7-15 chars to allow for different countries phone number systems.
    if (!phone.matches("^\\+?[0-9]{7,15}$")) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid phone number");
            return;
        }
    
    //hash + save. 
    try
        {
        //make salt and hash the password (FOR LOGIN)
            byte[] salt = makeSalt();
            String saltBase64 = Base64.getEncoder().encodeToString(salt);   //convert salt to base64
            String hashBase64 = hashSha256(password, salt);                 
            
        //encrpyt password
        //String encryptedPasswordBase64 = encryptAES(password);
        
        
        //file created where app runs
        File file = new File("users.txt");
        
        String line = String.join(",",username,saltBase64,hashBase64,email,phone,secQ);

        
        
        try(FileWriter fileWriter = new FileWriter(file, true))
            {
            fileWriter.write(line);
            fileWriter.write(System.lineSeparator());   //new line after each entry
            }
        }
    catch (Exception ex) 
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Problem creating account.");
        }
    //display "account creation" message if all details are filled in 
    javax.swing.JOptionPane.showMessageDialog(this, "Your account has been created. Thank you for joining us!");
    
    //clear fields on validating registration details 
    UsernameInput.setText("");
    PasswordInput.setText("");
    ConfirmPasswordInput.setText("");
    EmailInput.setText("");
    PhoneNumberInput.setText("");
    SecurityQInput.setText("");
    
     

        
        
    }//GEN-LAST:event_CreateAccountButtonActionPerformed

