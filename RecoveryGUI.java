    private String findAccount(String email, String phone, String secQ){
        try(BufferedReader br = new BufferedReader(new FileReader("users.txt"))){
            String line;
            while((line = br.readLine()) != null){
                String[] parts = line.split(",");
                
                if (parts.length>= 6){
                    String fileEmail = parts[3].trim();
                    String filePhone = parts[4].trim();
                    String fileSecQ = parts[5].trim();
                    if (email.equals(fileEmail) && phone.equals(filePhone) && secQ.equals(fileSecQ)) {
                        return parts[0];
                    }
                }
            }
        } catch (IOException e){
            javax.swing.JOptionPane.showMessageDialog(this, "error finding file" + e.getMessage());
        }
        return null;
    }

