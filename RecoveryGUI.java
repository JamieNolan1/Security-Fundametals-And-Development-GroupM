    private void ResetPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ResetPasswordActionPerformed
        //take user details from email, phone number and security question//
        
        String email = EmailRecovery.getText().trim();
        String phone = PhoneNumberRecovery.getText().trim();
        String secQ = SecurityQuestionRecovery.getText().trim();
        
        //check user input field are filled//
        
        if (email.isEmpty()||phone.isEmpty()||secQ.isEmpty())
            {
                javax.swing.JOptionPane.showMessageDialog(this, "Please Fill in all Fields");
                return;
            }
        
        //check if Email is in correct Format
        if (!email.matches("^[^@\\s]+@[^@\\s]+\\.[^@\\s]+$"))
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter correct Email Format");
            return;
        }
        
        //check if Phone Number is in correct Format
        
        if(!phone.matches("^\\+?[0-9]{7,15}$"))
        {
            javax.swing.JOptionPane.showMessageDialog(this,"Please enter correct Phone Format");
            return;
        }
        
        //after validation, search for the correct account in the .txt file, and grabs the usernameof that account, then opens the password reset Page
        String username = findAccount(email,phone,secQ);
        
        if (username != null) {
            //opens the ResetPasswordGUI under the found account
            ResetPasswordGUI resetPasswordGUI = new ResetPasswordGUI(username);
            resetPasswordGUI.setVisible(true);
            //closes the current page//
            this.dispose();
            //if there is no accounts in the txt file under this info://
        } else{
            javax.swing.JOptionPane.showMessageDialog(this,"No account found with that information, please create account");
            return;
                    
        }  
    }//GEN-LAST:event_ResetPasswordActionPerformed

